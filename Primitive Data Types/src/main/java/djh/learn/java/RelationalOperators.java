package djh.learn.java;

//==,!=,>,<,>=,<=
public class RelationalOperators {
    public static void main(String[] args) {

        boolean isActive = true;
        System.out.println(isActive);
        System.out.println(4 == 6);
        System.out.println(4 == 4);
        System.out.println(4 > 6);
        System.out.println(4 < 6);
        System.out.println(4 >= 6);
        System.out.println(4 <= 6);
        System.out.println(4 != 6);
    }
}
