package djh.learn.java;

public class IntegralTypes {
    public static void main(String[] args) {
        byte myByte = (byte)-129;
        short myShort = 32000;
        int myInt = 20000;
        long myLong = 922_337_203_685_477_500L;
        float myFloat = 2.3234644456F;
        double myDouble = 65.343343333889654337687;
        System.out.println(myByte);
        System.out.println(myLong);
        System.out.println(myFloat);
        System.out.println(myDouble);

    }
}
