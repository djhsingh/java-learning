package djh.learn.java;

public class AssignmentOperators {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(x);
        int y = x;
        x = 7;
        System.out.println(x);
        x += 4;
        System.out.println(x);
        x -= 3;
        System.out.println(x);
        x /= 2;
        System.out.println(x);
        x *= 4;
        System.out.println(x);
        System.out.println(y);
    }
}
